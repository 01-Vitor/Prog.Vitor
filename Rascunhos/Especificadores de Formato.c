#include <stdio.h>

/* 

Especificadores de formato printf
 
    %d: inteiro decimal
    %u: inteiro decimal sem sinal
    %f: ponto flutuante (com casa decimal)
    %c: caractere
    %s: string
    %p: ponteiro
    %x: hexadecimal
    %o: octal
    %e: notação científica
    %g: usa a notação mais curta entre %e e %f
    %ld: long decimal
    %lu: long decimal sem sinal
    %lld: long long decimal
    %llu: long long decimal sem sinal
    %lf: long double

    para unsigned int: %u (unsigned decimal);
    ex: scanf("%u", &variavel);

    para long int: %ld (long decimal)
    ex: scanf("%ld", &variavel);

    para long long int: %lld (long long decimal)
    ex: scanf("%lld", &variavel);

    para unsigned long int: %lu (unsigned long decimal)
    ex: scanf("%lu", &variavel);

    para unsigned long long int: %llu (long long decimal)
    ex: scanf("%llu", &variavel1);
    
Especificadores de Formato scanf
    
    %d: inteiro decimal
    %x: hexadecimal
    %f: ponto flutante (com casas decimais)
    %e: notação cientifica (com casas decimais)
    %c: caracteres alfanumericos
    %s: sequencia de caracteres alfanumericos
    [^chars]: lê todos os dados digitados, exceto os especificados em "chars", ex: não lê o 'enter'

*/